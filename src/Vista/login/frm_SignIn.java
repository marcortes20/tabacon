    public String validat_signin_data() {

        String empty_fields = "";

        if (txt_userName.getText().trim().isEmpty()) {

            txt_userName.requestFocus();

            empty_fields += "\nUsername.";

        }

        if (Password_field.getPassword().length == 0) {

            Password_field.requestFocus();

            empty_fields += "\nPassword.";

        }
        return empty_fields;
    }
