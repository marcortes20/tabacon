    private void btn_SignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SignInActionPerformed

        if (validat_signin_data().isEmpty()) {

            user.setUserName(txt_userName.getText());

            manaLogin.setPassword_logIN(user, Password_field.getPassword());

            if (manaLogin.validate_accound(user)) {
                
                manaLogin.getUser(user);

                contador = 0;

                JOptionPane.showMessageDialog(this, "welcome to Tabacon Thermal Resort & Spa", "Successfully logged in!", JOptionPane.INFORMATION_MESSAGE);
                
                Menu menu = new Menu(user);
                
                this.setVisible(false);
                
                menu.setVisible(true);

            } else {

                contador = contador + 1;

                JOptionPane.showMessageDialog(this, "Your username or password are wrong, try again", "Login denied!", JOptionPane.ERROR_MESSAGE);

                if (contador == 1) {

                    lb_intentosFallidos.setForeground(Color.GREEN);

                    lb_intentosFallidos.setText("Failed attempts: " + contador);

                } else if (contador == 2) {

                    lb_intentosFallidos.setForeground(Color.YELLOW);

                    lb_intentosFallidos.setText("Failed attempts: " + contador);

                } else {

                    this.dispose();

                }
            }
    }//GEN-LAST:event_btn_SignInActionPerformed
       else {
    

            JOptionPane.showMessageDialog(this, "EMPTY FIELDS:" + validat_signin_data(), "You must fill in all fields!", JOptionPane.ERROR_MESSAGE);

        }
    }
